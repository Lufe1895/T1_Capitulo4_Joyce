public class Month {
    private final Integer january;
    private final Integer february;
    private final Integer march;
    private final Integer april;
    private final Integer may;
    private final Integer june;
    private final Integer july;
    private final Integer august;
    private final Integer september;
    private final Integer october;
    private final Integer november;
    private final Integer december;
    
    public Month(){
        this.january=31;
        this.february=28;
        this.march=31;
        this.april=30;
        this.may=31;
        this.june=30;
        this.july=31;
        this.august=31;
        this.september=30;
        this.october=31;
        this.november=30;
        this.december=31;
    }

    /**
     * @return the january
     */
    public Integer getJanuary() {
        return january;
    }

    /**
     * @return the february
     */
    public Integer getFebruary() {
        return february;
    }

    /**
     * @return the march
     */
    public Integer getMarch() {
        return march;
    }

    /**
     * @return the april
     */
    public Integer getApril() {
        return april;
    }

    /**
     * @return the may
     */
    public Integer getMay() {
        return may;
    }

    /**
     * @return the june
     */
    public Integer getJune() {
        return june;
    }

    /**
     * @return the july
     */
    public Integer getJuly() {
        return july;
    }

    /**
     * @return the august
     */
    public Integer getAugust() {
        return august;
    }

    /**
     * @return the september
     */
    public Integer getSeptember() {
        return september;
    }

    /**
     * @return the october
     */
    public Integer getOctober() {
        return october;
    }

    /**
     * @return the november
     */
    public Integer getNovember() {
        return november;
    }

    /**
     * @return the december
     */
    public Integer getDecember() {
        return december;
    }
    
    
}
